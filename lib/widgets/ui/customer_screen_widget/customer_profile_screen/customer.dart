class Customer{

  String name = '';
  String surname = '';
  String email = '';
  String phone = '';
  String uid = '';


  void setUid(String value) {
    uid = value;
  }

  void setPhone(String value) {
    phone = value;
  }

  void setEmail(String value) {
    email = value;
  }

  void setSurname(String value) {
    surname = value;
  }

  void setName(String value) {
    name = value;
  }

}